insert into tradingpackages(
	AssetTypeId,
	BtcTransferConfirmations,
	DepositCommission,
	DepositCommissionChargeLimit,
	IsActive,
	MarginInterest,
	MarginRatio,
	MinWithdrawalCommission,
	Name,
	PackageUID,
	StopLossCommision,
	StopLossWarning,
	Symbol,
	TakeProfitCommission,
	TakeProfitReminder,
	TextResourceId,
	TradingMakerCommission,
	TradingTakerCommission,
	TriggerCommission,
	TriggerOrder,
	WithdrawalCommission
)
select 
	case when AssetTypeId = 0 
		then 0
		else 29
	end as AssetTypeId,
	BtcTransferConfirmations,
	DepositCommission,
	DepositCommissionChargeLimit,
	IsActive,
	MarginInterest,
	MarginRatio,
	MinWithdrawalCommission,
	Name,
	PackageUID,
	StopLossCommision,
	StopLossWarning,
	REPLACE(Symbol,'TNB', 'XLM'),
	TakeProfitCommission,
	TakeProfitReminder,
	TextResourceId,
	TradingMakerCommission,
	TradingTakerCommission,
	TriggerCommission,
	TriggerOrder,
	WithdrawalCommission
from tradingpackages where symbol like '%TNB%'

